package com.JavaSE8.fundation;

public class Demo01 {

	public static void main(String[] args) {
		// Long, String, etc is not the primitive type
		// So can be used as a variable
		long Long = 1L;
		int String = 123_456;
		System.out.println(Long);
		System.out.println(String);
		
	}
}
